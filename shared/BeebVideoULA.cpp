// Resize screen if it doesn't already have the correct size
void BeebVideoULA::updateScreenSz(int fullW, int fullH, int activeW, int activeH)
{
	if (fullW != mScreenW || fullH != mScreenH) {
		mScreenW = fullW;
		mScreenH = fullH;
		if (DBG_LEVEL_DEV(this,DBG_VERBOSE))
			cout << "create display bitmap " << dec << mScreenW << " x " << mScreenH << " (" << activeW << " x " << activeH << ")\n";
		al_resize_display(mDisplay, mScreenW, mScreenH);
		unlockDisplay();
		al_destroy_bitmap(mDisplayBitmap);	
		mDisplayBitmap = al_create_bitmap(mScreenW, mScreenH);
		al_set_new_bitmap_flags(mBitMapFlags);
		al_clear_to_color(black);
		lockDisplay();
		mMaxDisplayBitmap_p = (BITMAP_PTR) ((LOCKED_BITMAP_PTR) mLockedDisplayBitMap->data + mLockedDisplayBitMap->pitch * mScreenH);
	}
}

bool BeebVideoULA::validateInternalState(uint8_t newControlRegisterValue)
{
	uint8_t old_control_register_val = mControlRegister;
	mControlRegister = newControlRegisterValue;

	int p_scan_line = mScanLine;
	int p_scan_lines = mScreenScanLines;
	int p_vertical_syn_pos = mVerticalSyncPos;
	int p_pixels_per_byte = mPixelsPerCharacter;

	bool teletext = getCRField(CR_TELETEXT) == 1;
	bool p_teletext = getCRField(old_control_register_val, CR_TELETEXT);

	// Although the scroll context is updated based on C1C0 input changes in processPortUpdate() it could have the wrong value for any mode
	// if the C1C0 inputs are updated BEFORE the Control Register bit for teletext is changed. Therefore update the constant again
	// if the teletext bit was changed.
	if (teletext != p_teletext)
		updateHwScrollConstant();

	if (!initialised()) {
		mScanLine = 0;
		mScreenScanLines = 312;
		mVerticalSyncPos = 280;
		return false;
	}

	mScanLine = getScreenScanLine();
	mScreenScanLines = getScreenScanLines();
	mVerticalSyncPos = getVerticalSyncLine();

	mActiveLines = getActiveLines();

	if (getCRField(CR_CLOCK_RATE) == 1)
		updatePort(CRTC_CLK, 2);
	else
		updatePort(CRTC_CLK, 1);

	mNCols = (1 << getCRField(CR_N_COLS)) * 10;
	mPixelRate = 1 << (getCRField(CR_N_COLS) + 1); // pixel rate: 2, 4, 8 or 16 MHz



	//
	//	Pixels per memory byte (mPixelsPerCharacter), "big" pixels per column symbol (mHzPixelsPerSymbol) and size of "big" pixel (mPixelW)
	//
	//	As configured by the Video ULA Control Register	(CR)							Given by Video ULA PR and additional CRTC configuration
	//	===================================================								==========================================================
	//	No of cols	Pixel rate	Pixels/Byte				CRTC Clk		Colours			Total columns	Pixels/symb				Pixel size	Scr.pix./symb	Modes
	//							(mPixelsPerCharacter)													(mHzPixelsPerSymbol)	(mPixelW)
	//	==========	==========	=====================	========		=======			=============	====================	==========	==============	=====
	//	10			2 MHz		1						
	//	20			4 MHz		2:2 5:4					0-3:2 4-7:1		2:16 5:4		2:128,5:64		8						4			8				2,5
	//	40			8 MHz		1:4 4,6:8 7:N/A			0-3:2 4-7:1		1:4 4,6:2 7:N/A	1:128 4,6,7:64	8 (7:16)				2 (7:1)		8 (7:16)		1,4,6,7
	//	80			16 MHz		8						0-3:2 4-7:1		0,3:2			128				8						1			8				0,3
	//

	// Pixel width in "actual pixels" for a 640-pixel wide screen
	mPixelW = 16 / mPixelRate; 
	if (teletext)
		mPixelW = 1;

	// Pixels per memory byte
	// Round to consider subtle changes in displayed characters per line
	mPixelsPerCharacter = (int)round(8.0 * mNCols / getActiveCharsPerLine());
	if (mPixelsPerCharacter > 8) {
		return false;
	}
	if (teletext)
		mPixelsPerCharacter = 16; // as 12 pixels have been stretched to 16 by the TCG

	// "big" pixels per symbol
	mHzPixelsPerSymbol = 8;
	if (teletext)
		mHzPixelsPerSymbol = 16;

	// Vertical pixels per character row is normally the same as twice the no of raster lines
	// except for the interlace video mode where it is identical to the raster lines
	mVtPixelsPerRow = getCharScanLines();
	if (!mCRTC->interlacedVideo())
		mVtPixelsPerRow *= 2;

#ifdef DBG_ON
	if ((
		((mControlRegister ^ newControlRegisterValue) & 0xfe) != 0 ||
		mPixelsPerCharacter != p_pixels_per_byte ||
		mScreenScanLines != p_scan_lines ||
		mVerticalSyncPos != p_vertical_syn_pos
		)
		&& (DBG_LEVEL_DEV(this,DBG_VERBOSE))
	) {
		cout << "\n" << dec;
		cout << "Video ULA Control Register: 0x" << hex << (int)mControlRegister << dec << "\n";
		cout << "Video ULA PixelRate:       " << mPixelRate << " MHz\n";
		cout << "Video ULA PixelWidth:      " << (int)mPixelW << "\n";
		cout << "Video ULA Pixels/byte:     " << (int)mPixelsPerCharacter << "\n";
		cout << "Video ULA CRTC Clock:      " << (int)mCRTC_CLK << " MHz\n";
		cout << "Video ULA No of cols:      " << dec << mNCols << "\n";
		cout << "Video ULA Teletext:        " << (getCRField(CR_TELETEXT) ? "ON" : "OFF") << "\n";
		cout << "Video ULA Cursor Segments: " << hex << (int)getCRField(CR_CURSOR_SEGMENT) << "\n";
		cout << "Video ULA Pixel Width:     " << dec << (int)mPixelW << "\n";
		cout << "Video ULA Pixels/byte:     " << dec << mPixelsPerCharacter << "\n";
		cout << "Video ULA Scan Line:       " << dec << mScanLine << "\n";
		cout << "Video ULA Scan Lines:      " << dec << mScreenScanLines << "\n";
		cout << "Video ULA Vertical Sync:   " << dec << mVerticalSyncPos << "\n";
		cout << "\n";
	}
#endif
	

	return true;
}

bool BeebVideoULA::write(uint16_t adr, uint8_t data)
{

	if (!VideoDisplayUnit::selected(adr))
		return false;

	uint16_t a = adr - mMemorySpace.adr;

	if (a == 0) {

		validateInternalState(data);

	}
	else if (a == 1) {
		uint8_t pa = (data >> 4) & 0xf;
		mPaletteMem[pa] = data & 0xf;
	}

	
	
	// Call parent class to trigger scheduling of other devices when applicable
	return VideoDisplayUnit::triggerAfterWrite(adr, data);
}

